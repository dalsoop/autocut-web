export function makeI18n(initialLang = "ko") {
  const { reactive, computed } = Vue
  const state = reactive({ lang: localStorage.getItem("autocut_lang") || initialLang })
  const t = computed(() => I18N[state.lang] || I18N.ko)
  function setLang(l) { state.lang = l; localStorage.setItem("autocut_lang", l) }
  return { state, t, setLang }
}
